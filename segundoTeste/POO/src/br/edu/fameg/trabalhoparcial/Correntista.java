package br.edu.fameg.trabalhoparcial;

public class Correntista {
	private String nome;
	private String cpf;

	public String getNome() {
		return nome;
	}

	public String getCpf() {
		return cpf;
	}

	public Correntista(String nome, String cpf) {
		super();
		this.nome = nome;
		this.cpf = cpf;
	}

}
